package EmployEntry;
public class EmployeeEntry {
	private int id;
	private String name;  
	private int age;  
	private String dept;

	public EmployeeEntry(){}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String Dept) {
		this.dept = dept;
	}  	

}
